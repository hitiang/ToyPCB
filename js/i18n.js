    var supportedLangs = Object.keys(translations);

    function detectLanguage() {
        var langs = navigator.languages || [navigator.language || navigator.userLanguage || 'en'];
        for (var i = 0; i < langs.length; i++) {
            var code = langs[i].toLowerCase();
            // Exact match (e.g. "en", "zh")
            if (translations[code]) return code;
            // Prefix match (e.g. "zh-CN" → "zh", "pt-BR" → "pt")
            var prefix = code.split('-')[0];
            if (translations[prefix]) return prefix;
        }
        return 'en';
    }

    function t(key) {
        var lang = translations[currentLang];
        if (lang && lang[key] !== undefined) return lang[key];
        return translations.en[key] || key;
    }

    function applyLanguage(lang) {
        if (!translations[lang]) lang = 'en';
        currentLang = lang;

        // Update all elements with data-i18n attribute
        var els = document.querySelectorAll('[data-i18n]');
        for (var i = 0; i < els.length; i++) {
            var key = els[i].getAttribute('data-i18n');
            els[i].textContent = t(key);
        }

        // Update elements with data-i18n-placeholder
        var placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        for (var i = 0; i < placeholders.length; i++) {
            var key = placeholders[i].getAttribute('data-i18n-placeholder');
            placeholders[i].placeholder = t(key);
        }

        // Update <option> elements with data-i18n
        var options = document.querySelectorAll('option[data-i18n]');
        for (var i = 0; i < options.length; i++) {
            var key = options[i].getAttribute('data-i18n');
            options[i].textContent = t(key);
        }

        // Update page title
        document.title = t('title');
    }

    function getCurrentLang() {
        return currentLang;
    }

    return {
        t: t,
        applyLanguage: applyLanguage,
        detectLanguage: detectLanguage,
        getCurrentLang: getCurrentLang
    };
